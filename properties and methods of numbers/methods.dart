import 'dart:io';

void main(){
  stdout.writeln(6.6.ceil());
  print(-7.2.abs());

  stdout.writeln(8.3.floor());
  stdout.writeln(5.compareTo(4));
  stdout.writeln(7.remainder(5));
  stdout.writeln(6.5.round());
  stdout.writeln(5.toDouble());
  stdout.writeln(7.2.toInt());
  stdout.writeln(6.toString()+"7");
  stdout.writeln(8.truncate());
}